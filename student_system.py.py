# Matrix Operations using Lists

def matrix_add(A, B):
    """Return element-wise addition of matrices A and B"""

    # Check dimension compatibility
    if len(A) != len(B) or len(A[0]) != len(B[0]):
        print("Matrix addition not possible: dimension mismatch.")
        return None

    result = [
        [A[i][j] + B[i][j] for j in range(len(A[0]))]
        for i in range(len(A))
    ]

    return result


def matrix_transpose(matrix):
    """Return transpose of matrix"""

    # Using nested list comprehension
    return [
        [row[i] for row in matrix]
        for i in range(len(matrix[0]))
    ]

    # Alternative (Pythonic):
    # return [list(col) for col in zip(*matrix)]


def matrix_multiply(A, B):
    """Return matrix multiplication result of A and B"""

    # Check if multiplication is possible
    if len(A[0]) != len(B):
        print("Matrix multiplication not possible: dimension mismatch.")
        return None

    result = [
        [
            sum(a * b for a, b in zip(row_a, col_b))
            for col_b in zip(*B)
        ]
        for row_a in A
    ]

    return result


