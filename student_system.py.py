"""Part C — Interview Ready

Q1 — Shallow Copy vs Deep Copy

1. Shallow Copy

    A shallow copy creates a new outer container but does not copy nested objects.
    Inner objects still reference the same memory locations.

    Meaning:

          Outer list → new object

          Inner lists → same objects
"""

#Example
import copy

original = [[1, 2], [3, 4]]

shallow = copy.copy(original)

shallow[0][0] = 99

print(original)
print(shallow)

"""**Why It Fails**

Because both lists point to the same nested list.

Memory representation:

    original ---> [ refA , refB ]
    shallow  ---> [ refA , refB ]

    refA ---> [1,2]
    refB ---> [3,4]

Changing refA affects both.

2. **Deep Copy**

    A deep copy duplicates the entire structure including all nested objects.
"""

import copy

original = [[1, 2], [3, 4]]

deep = copy.deepcopy(original)

deep[0][0] = 99

print(original)
print(deep)

"""Memory representation:

    original ---> [ refA , refB ]
    deep     ---> [ refC , refD ]

    refA -> [1,2]
    refB -> [3,4]

    refC -> [1,2]
    refD -> [3,4]

All objects are independent.

**When Deep Copy Is Required**

Use deep copy when:

      

     1.  Data structures contain nested lists/dictionaries

     2. Modifying a copy must not affect the original

     3. Working with matrix data

     4. Working with config/state cloning

     5. Building algorithms that mutate structures



Typical cases:

     1. Graph algorithms

     2. Backtracking

     3. Matrix transformations

     4. Game state simulations

Q2 — List Rotation
Problem

Rotate list to the right by k positions.
"""

def rotate_list(lst, k):

    n = len(lst)

    if n == 0:
        return lst

    k = k % n   # handle k > length

    return lst[-k:] + lst[:-k]

print(rotate_list([1,2,3,4,5], 2))

