# 5. Improved Version (Remove Duplicate Pairs)

# Using index-based comprehension.

def find_pairs_unique(nums, target):

    return [
        (nums[i], nums[j])
        for i in range(len(nums))
        for j in range(i + 1, len(nums))
        if nums[i] + nums[j] == target
    ]

print(find_pairs_unique([1,2,3,4,5], 6))

print(find_pairs_unique([1,1,1], 2))

"""Best Optimized Solution (O(n))"""

def pair_sum_optimized(nums, target):

    seen = set()
    pairs = set()

    for num in nums:

        complement = target - num

        if complement in seen:
            pairs.add(tuple(sorted((num, complement))))

        seen.add(num)

    return list(pairs)

print(pair_sum_optimized([1,2,3,4,5], 6))

print(pair_sum_optimized([1,1,1], 2))

